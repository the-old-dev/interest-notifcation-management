package org.inm.website;

import java.net.URL;

public class Subscribable {

	private String name;
	private URL url;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public URL getUrl() {
		return url;
	}

	public void setUrl(URL url) {
		this.url = url;
	}

}
